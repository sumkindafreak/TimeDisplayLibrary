void TimeDisplayLibrary::clear() {
    for (int i = 0; i < _time_display.numPixels(); i++) {
        _time_display.setPixelColor(i, 0); // Set each pixel to 'off'
    }
    _time_display.show(); // Push the changes to the LEDs
}
